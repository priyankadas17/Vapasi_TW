package Operators;

public class AreaandPerimeterOfRec {
    public static void main(String[] args){
        int length=5;
        int breadth=7;
        //Calculate the area of Rectangle
        int AreaOfRec=length*breadth;
        //Calculate the perimeter of Rectangle
        int PerimeterOfRec=2*(length+breadth);
        //Print the area of Rectangle
        System.out.println("The Area of Rectangle is "+AreaOfRec);
        //Print the Perimeter of Rectangle
        System.out.println("The Perimeter of Rectangle is "+PerimeterOfRec);
    }
}
